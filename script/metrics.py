"""Anomaly metrics."""
import numpy as np
from sklearn import metrics

from skimage import measure
def compute_imagewise_retrieval_metrics(
    anomaly_prediction_weights, anomaly_ground_truth_labels, inference
):
    """
    Computes retrieval statistics.

    Args:
        anomaly_prediction_weights: [np.array or list] [N] Assignment weights
                                    per image. Higher indicates higher
                                    probability of being an anomaly.
        anomaly_ground_truth_labels: [np.array or list] [N] Binary labels - 1
                                    if image is an anomaly, 0 if not.
    """
    fpr, tpr, thresholds = metrics.roc_curve(
        anomaly_ground_truth_labels, anomaly_prediction_weights
    )
    auroc = metrics.roc_auc_score(
        anomaly_ground_truth_labels, anomaly_prediction_weights
    )
    if inference:
        precision, recall, thresholds = metrics.precision_recall_curve(anomaly_ground_truth_labels,anomaly_prediction_weights)


    return {
        "auroc": auroc, 
        "fpr": fpr, 
        "tpr": tpr, 
        "threshold": thresholds, 
        # "ap": ap, 
        # "f1": f1,
    }


